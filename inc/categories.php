/**
 * Import the default categories into the listing_category taxonomy.
 * Skips categories that already exist (matched by slug).
 *
 * @return int Number of categories imported.
 */
function dxadult_import_default_categories() {
	$categories = dxadult_get_default_categories();
	$imported  = 0;
	foreach ( $categories as $cat ) {
		if ( ! term_exists( $cat['slug'], 'listing_category' ) ) {
			$result = wp_insert_term(
				$cat['name'],
				'listing_category',
				array(
					'slug'        => $cat['slug'],
					'description' => $cat['description'],
				)
			);
			if ( ! is_wp_error( $result ) ) {
				++$imported;
			}
		}
	}
	return $imported;
}
add_action( 'after_switch_theme', 'dxadult_import_default_categories' );

/**
 * Add an admin notice after activation showing how many categories were imported.
 */
function dxadult_categories_activation_notice() {
	$transient_key = 'dxadult_categories_imported_count';
	$count = get_transient( $transient_key );
	if ( false === $count ) {
		return;
	}
	?>
	<div class="notice notice-success is-dismissible">
		<p>
			<strong><?php esc_html_e( 'DirectoryX Adult:', 'directoryx-adult' ); ?></strong>
			<?php
			printf(
				/* translators: %d: number of categories imported */
				esc_html( _n( 'Imported %d default category.', 'Imported %d default categories.', $count, 'directoryx-adult' ) ),
				(int) $count
			);
			?>
			<?php esc_html_e( 'You can rename or remove any of them under Listings → Categories.', 'directoryx-adult' ); ?>
		</p>
	</div>
	<?php
	delete_transient( $transient_key );
}
add_action( 'admin_notices', 'dxadult_categories_activation_notice' );

/**
 * Count categories after import on activation.
 */
function dxadult_categories_activation_count() {
	// Only run right after theme activation.
	$flag = get_transient( 'dxadult_theme_activated' );
	if ( ! $flag ) {
		return;
	}
	$count = dxadult_import_default_categories();
	set_transient( 'dxadult_categories_imported_count', $count, MINUTE_IN_SECONDS * 5 );
	delete_transient( 'dxadult_theme_activated' );
}
add_action( 'admin_init', 'dxadult_categories_activation_count' );

/**
 * Set a transient on theme activation so the next admin_init can count imports.
 */
function dxadult_set_activation_flag() {
	set_transient( 'dxadult_theme_activated', 1, MINUTE_IN_SECONDS );
}
add_action( 'after_switch_theme', 'dxadult_set_activation_flag' );
